package pl.kurcaba.Strategy;

/**
 * Class represents one employee
 */
public class Employee {
    private int salary;

    /**
     *
     * @return employee salary
     */
    public int getSalary() {
        return salary;
    }

    /**
     *
     * @param salary new employee salary
     */
    public void setSalary(int salary) {
        this.salary = salary;
    }
}
